/**
 * Fun√ß√£o utilit√°ria para debounce de eventos (evita execu√ß√£o excessiva).
 * @param {Function} func - Fun√ß√£o a ser executada
 * @param {number} wait - Tempo de espera em ms
 * @returns {Function} - Fun√ß√£o com debounce aplicado
 */
function debounce(func, wait) {
    let timeout;
    return function executedFunction(...args) {
        const later = () => {
            clearTimeout(timeout);
            func(...args);
        };
        clearTimeout(timeout);
        timeout = setTimeout(later, wait);
    };
}

/**
 * Fun√ß√£o utilit√°ria para throttle de eventos (limita frequ√™ncia de execu√ß√£o).
 * @param {Function} func - Fun√ß√£o a ser executada
 * @param {number} limit - Limite de tempo em ms
 * @returns {Function} - Fun√ß√£o com throttle aplicado
 */
function throttle(func, limit) {
    let inThrottle;
    return function() {
        const args = arguments;
        const context = this;
        if (!inThrottle) {
            func.apply(context, args);
            inThrottle = true;
            setTimeout(() => inThrottle = false, limit);
        }
    };
}

