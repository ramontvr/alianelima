/**
 * Aplica o filtro selecionado aos itens do portf√≥lio com anima√ß√£o suave.
 * @param {string} filter - O filtro a ser aplicado ('*' para todos, ou classe espec√≠fica)
 */
function applyPortfolioFilter(filter) {
    GmaakeupApp.portfolioItems.forEach(item => {
        const shouldShow = filter === '*' || item.classList.contains(filter.substring(1));
        
        if (shouldShow) {
            // Mostrar item com anima√ß√£o
            item.style.display = 'block';
            setTimeout(() => {
                item.style.opacity = '1';
                item.style.transform = 'scale(1)';
            }, 10);
        } else {
            // Esconder item com anima√ß√£o
            item.style.opacity = '0';
            item.style.transform = 'scale(0.8)';
            setTimeout(() => {
                item.style.display = 'none';
            }, GmaakeupApp.config.animationDuration);
        }
    });
}

