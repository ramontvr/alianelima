// ===== CONFIGURA√á√ÉO DE ANIMA√á√ïES DE SCROLL =====
/**
 * Configura anima√ß√µes que s√£o ativadas quando elementos entram na viewport.
 */
function setupScrollAnimations() {
    console.log('‚ú® Configurando anima√ß√µes de scroll...');
    
    // Verificar suporte ao Intersection Observer
    if (!('IntersectionObserver' in window)) {
        console.warn('‚ö†Ô∏è Intersection Observer n√£o suportado');
        return;
    }
    
    // Configurar observer para anima√ß√µes
    const animationObserver = new IntersectionObserver(
        handleIntersection,
        {
            threshold: 0.1,
            rootMargin: '0px 0px -50px 0px'
        }
    );
    
    // Observar elementos que devem ser animados
    const animatedElements = document.querySelectorAll(
        '.service-card, .news-card, .about-content, .hero-content'
    );
    
    animatedElements.forEach(element => {
        element.style.opacity = '0';
        element.style.transform = 'translateY(30px)';
        element.style.transition = 'opacity 0.6s ease, transform 0.6s ease';
        animationObserver.observe(element);
    });
    
    console.log('‚úÖ Anima√ß√µes de scroll configuradas');
}

/**
 * Manipula a interse√ß√£o de elementos com a viewport para anima√ß√µes.
 * @param {IntersectionObserverEntry[]} entries - Entradas do observer
 */
function handleIntersection(entries) {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            // Animar entrada do elemento
            entry.target.style.opacity = '1';
            entry.target.style.transform = 'translateY(0)';
            
            // Parar de observar este elemento
            entry.target.classList.add('animated');
        }
    });
}

