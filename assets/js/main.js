/**
 * Valida se um email tem formato v√°lido usando regex.
 * @param {string} email - O email a ser validado
 * @returns {boolean} - True se o email for v√°lido
 */
function isValidEmail(email) {
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    return emailRegex.test(email);
}

/**
 * Exibe feedback visual para formul√°rios (sucesso ou erro).
 * @param {HTMLElement} input - Campo de input
 * @param {string} message - Mensagem a ser exibida
 * @param {string} type - Tipo do feedback ('success' ou 'error')
 */
function showFormFeedback(input, message, type) {
    // Remover feedback anterior
    const existingFeedback = input.parentNode.querySelector('.form-feedback');
    if (existingFeedback) {
        existingFeedback.remove();
    }
    
    // Criar elemento de feedback
    const feedback = document.createElement('div');
    feedback.className = `form-feedback ${type}`;
    feedback.textContent = message;
    feedback.style.cssText = `
        margin-top: 5px;
        font-size: 0.875rem;
        color: ${type === 'error' ? '#e74c3c' : '#27ae60'};
        opacity: 0;
        transition: opacity 0.3s ease;
    `;
    
    // Inserir feedback
    input.parentNode.appendChild(feedback);
    
    // Animar entrada
    setTimeout(() => {
        feedback.style.opacity = '1';
    }, 10);
    
    // Remover feedback ap√≥s 5 segundos
    setTimeout(() => {
        if (feedback.parentNode) {
            feedback.style.opacity = '0';
            setTimeout(() => {
                if (feedback.parentNode) {
                    feedback.remove();
                }
            }, 300);
        }
    }, 5000);
}

/**
 * Simula o envio de um formul√°rio com feedback visual para o usu√°rio.
 * @param {HTMLElement} input - Campo de input
 * @param {HTMLElement} button - Bot√£o de envio
 * @param {string} formType - Tipo do formul√°rio para logging
 */
function simulateFormSubmission(input, button, formType) {
    const originalButtonText = button.textContent;
    
    // Mostrar estado de carregamento
    button.textContent = 'Enviando...';
    button.disabled = true;
    input.disabled = true;
    
    // Simular delay de envio
    setTimeout(() => {
        // Restaurar estado original
        button.textContent = originalButtonText;
        button.disabled = false;
        input.disabled = false;
        
        // Mostrar feedback de sucesso
        showFormFeedback(input, 'Obrigado! Voc√™ foi inscrito com sucesso.', 'success');
        
        // Limpar campo
        input.value = '';
        
        console.log(`üìß ${formType} enviado com sucesso (simulado)`);
    }, 2000);
}

